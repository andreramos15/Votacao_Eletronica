    private void btStartServerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btStartServerActionPerformed
        try {

            spMyServerPort.setVisible(false);
            btStartServer.setVisible(false);
            int port = (int) spMyServerPort.getValue();
            node = new Remote(port);

            node.addListener(listeners);

            RMI.startRemoteObject(node, port, Remote.remoteName);
            logMessage(node.getAdress());
            jTextArea1.setText(spMyServerPort.getValue().toString());

        } catch (Exception ex) {
            logError(ex.getMessage());
        }
    }//GEN-LAST:event_btStartServerActionPerformed

